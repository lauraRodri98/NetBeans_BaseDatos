    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {                                       
        //añadimos los datos que hemos encontrado en los textField
        String equiLocal="";
        String equiVisi="";
        int puntosLocal=0;
       
        int puntosVisi=0;
        //Añadimos en funcion del codigo el filtro
        codigo = (String)textC.getSelectedItem();

         try
        {
            sql = "SELECT equipo_local, equipo_visitante, puntos_local, puntos_visitante FROM partidos where codigo = " + codigo;
            con = (Connection)DriverManager.getConnection(url,"root","");
            sentencia = con.createStatement();
            rs = sentencia.executeQuery(sql);

            while(rs.next()){
                equiLocal = rs.getString("equipo_local");
                equiVisi = rs.getString("equipo_visitante");
                puntosLocal = rs.getInt("puntos_local");
                puntosVisi = rs.getInt("puntos_visitante");
            }

            }catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, ex);

        } finally {
            // Cerrar conexiones y resultado
            try {
                if (rs != null) {
                    rs.close();
                }
                if (sentencia != null) {
                    sentencia.close();
                }
                if (con != null) {
                    con.close();
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
        text1.setText(equiLocal);
