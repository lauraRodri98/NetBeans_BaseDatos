    private void introducirCod ()
    {
         // inserta el cod en el combobox
        ArrayList <String> listaCod = new ArrayList<>();
        Integer cod = 0;
        String codString = "";

        try
        {
            sql = "SELECT codigo from partidos order by codigo asc";
            con = (Connection)DriverManager.getConnection(url,"root","");
            sentencia = con.createStatement();
            rs = sentencia.executeQuery(sql);

            while(rs.next()){
                cod = rs.getInt("codigo");
                codString = String.valueOf(cod) + "";
                listaCod.add(codString);
            }
        }catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, ex);

        } finally {
            // Cerrar conexiones y resultado
            try {
                if (rs != null) {
                    rs.close();
                }
                if (sentencia != null) {
                    sentencia.close();
                }
                if (con != null) {
                    con.close();
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
        //DefaultComboBoxModel d = new DefaultComboBoxModel(listaCodArray);
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>(listaCod.toArray(new String[0]));
        textC.setModel(model);
        
